'use client'
import * as Clerk from '@clerk/elements/common'
import * as SignIn from '@clerk/elements/sign-in'
import { Button } from '@/components/ui/button'
import {
    Card,
    CardContent,
    CardDescription,
    CardFooter,
    CardHeader,
    CardTitle,
} from '@/components/ui/card'
import { Input } from '@/components/ui/input'
import { Label } from '@/components/ui/label'
import { Loader2 } from 'lucide-react'
import { useUser } from '@clerk/nextjs'
import { useEffect } from 'react'
import { useRouter } from 'next/navigation'

export default function SignInPage() {
    const { isLoaded, isSignedIn, user } = useUser()
    const router = useRouter()

    useEffect(() => {
        if (isSignedIn && user?.publicMetadata?.role) {
            router.push(`/${user.publicMetadata.role}`)
        }
    }, [isSignedIn, user, router])

    // Don't render anything while checking auth state
    if (!isLoaded) {
        return (
            <div className="flex w-full items-center px-4 sm:justify-center justify-center h-screen">
                <div className="flex items-center justify-center">
                    <Loader2 className="size-8 animate-spin" />
                </div>
            </div>
        )
    }

    // If user is already signed in, show loading while redirect happens
    if (isSignedIn) {
        return (
            <div className="flex w-full items-center px-4 sm:justify-center justify-center h-screen">
                <div className="flex items-center justify-center">
                    <Loader2 className="size-8 animate-spin" />
                    <span className="ml-2">Redirecting...</span>
                </div>
            </div>
        )
    }

    return (
        <div className="flex w-full items-center px-4 sm:justify-center justify-center h-screen">
            <SignIn.Root>
                <Clerk.Loading>
                    {(isGlobalLoading) => (
                        <>
                            <SignIn.Step name="start">
                                <Card className="w-full sm:w-96">
                                    <CardHeader>
                                        <CardTitle>Sign in to Edu CRM</CardTitle>
                                        <CardDescription>Welcome back! Please sign in to continue</CardDescription>
                                    </CardHeader>
                                    <CardContent className="grid gap-y-4">
                                        <Clerk.Field name="identifier" className="space-y-2">
                                            <Clerk.Label asChild>
                                                <Label>Email address</Label>
                                            </Clerk.Label>
                                            <Clerk.Input type="email" required asChild>
                                                <Input />
                                            </Clerk.Input>
                                            <Clerk.FieldError className="block text-sm text-destructive" />
                                        </Clerk.Field>
                                    </CardContent>
                                    <CardFooter>
                                        <div className="grid w-full gap-y-4">
                                            <SignIn.Action submit asChild>
                                                <Button disabled={isGlobalLoading}>
                                                    <Clerk.Loading>
                                                        {(isLoading) => {
                                                            return isLoading ? (
                                                                <Loader2 className="size-4 animate-spin" />
                                                            ) : (
                                                                'Continue'
                                                            )
                                                        }}
                                                    </Clerk.Loading>
                                                </Button>
                                            </SignIn.Action>
                                        </div>
                                    </CardFooter>
                                </Card>
                            </SignIn.Step>

                            <SignIn.Step name="choose-strategy">
                                <Card className="w-full sm:w-96">
                                    <CardHeader>
                                        <CardTitle>Use another method</CardTitle>
                                        <CardDescription>
                                            Facing issues? You can use any of these methods to sign in.
                                        </CardDescription>
                                    </CardHeader>
                                    <CardContent className="grid gap-y-4">
                                        <SignIn.SupportedStrategy name="email_code" asChild>
                                            <Button type="button" variant="link" disabled={isGlobalLoading}>
                                                Email code
                                            </Button>
                                        </SignIn.SupportedStrategy>
                                        <SignIn.SupportedStrategy name="password" asChild>
                                            <Button type="button" variant="link" disabled={isGlobalLoading}>
                                                Password
                                            </Button>
                                        </SignIn.SupportedStrategy>
                                    </CardContent>
                                    <CardFooter>
                                        <div className="grid w-full gap-y-4">
                                            <SignIn.Action navigate="previous" asChild>
                                                <Button disabled={isGlobalLoading}>
                                                    <Clerk.Loading>
                                                        {(isLoading) => {
                                                            return isLoading ? (
                                                                <Loader2 className="size-4 animate-spin" />
                                                            ) : (
                                                                'Go back'
                                                            )
                                                        }}
                                                    </Clerk.Loading>
                                                </Button>
                                            </SignIn.Action>
                                        </div>
                                    </CardFooter>
                                </Card>
                            </SignIn.Step>

                            <SignIn.Step name="verifications">
                                <SignIn.Strategy name="password">
                                    <Card className="w-full sm:w-96">
                                        <CardHeader>
                                            <CardTitle>Enter your password</CardTitle>
                                            <p className="text-sm text-muted-foreground">
                                                Welcome back <SignIn.SafeIdentifier />
                                            </p>
                                        </CardHeader>
                                        <CardContent className="grid gap-y-4">
                                            <Clerk.Field name="password" className="space-y-2">
                                                <Clerk.Label asChild>
                                                    <Label>Password</Label>
                                                </Clerk.Label>
                                                <Clerk.Input type="password" asChild>
                                                    <Input />
                                                </Clerk.Input>
                                                <Clerk.FieldError className="block text-sm text-destructive" />
                                            </Clerk.Field>
                                        </CardContent>
                                        <CardFooter>
                                            <div className="grid w-full gap-y-4">
                                                <SignIn.Action submit asChild>
                                                    <Button disabled={isGlobalLoading}>
                                                        <Clerk.Loading>
                                                            {(isLoading) => {
                                                                return isLoading ? (
                                                                    <Loader2 className="size-4 animate-spin" />
                                                                ) : (
                                                                    'Continue'
                                                                )
                                                            }}
                                                        </Clerk.Loading>
                                                    </Button>
                                                </SignIn.Action>
                                                <SignIn.Action navigate="choose-strategy" asChild>
                                                    <Button type="button" size="sm" variant="link">
                                                        Use another method
                                                    </Button>
                                                </SignIn.Action>
                                            </div>
                                        </CardFooter>
                                    </Card>
                                </SignIn.Strategy>

                                <SignIn.Strategy name="email_code">
                                    <Card className="w-full sm:w-96">
                                        <CardHeader>
                                            <CardTitle>Check your email</CardTitle>
                                            <CardDescription>
                                                Enter the verification code sent to your email
                                            </CardDescription>
                                            <p className="text-sm text-muted-foreground">
                                                Welcome back <SignIn.SafeIdentifier />
                                            </p>
                                        </CardHeader>
                                        <CardContent className="grid gap-y-4">
                                            <Clerk.Field name="code">
                                                <Clerk.Label className="sr-only">Email verification code</Clerk.Label>
                                                <div className="grid gap-y-2 items-center justify-center">
                                                    <div className="flex justify-center text-center">
                                                        <Clerk.Input
                                                            type="otp"
                                                            autoSubmit
                                                            className="flex justify-center has-disabled:opacity-50"
                                                            render={({ value, status }) => {
                                                                return (
                                                                    <div
                                                                        data-status={status}
                                                                        className="relative flex h-9 w-9 items-center justify-center border-y border-r border-input text-sm shadow-sm transition-all first:rounded-l-md first:border-l last:rounded-r-md data-[status=selected]:ring-1 data-[status=selected]:ring-ring data-[status=cursor]:ring-1 data-[status=cursor]:ring-ring"
                                                                    >
                                                                        {value}
                                                                    </div>
                                                                )
                                                            }}
                                                        />
                                                    </div>
                                                    <Clerk.FieldError className="block text-sm text-destructive text-center" />
                                                    <SignIn.Action
                                                        asChild
                                                        resend
                                                        className="text-muted-foreground"
                                                        fallback={({ resendableAfter }) => (
                                                            <Button variant="link" size="sm" disabled>
                                                                Didn&apos;t receive a code? Resend (
                                                                <span className="tabular-nums">{resendableAfter}</span>)
                                                            </Button>
                                                        )}
                                                    >
                                                        <Button variant="link" size="sm">
                                                            Didn&apos;t receive a code? Resend
                                                        </Button>
                                                    </SignIn.Action>
                                                </div>
                                            </Clerk.Field>
                                        </CardContent>
                                        <CardFooter>
                                            <div className="grid w-full gap-y-4">
                                                <SignIn.Action submit asChild>
                                                    <Button disabled={isGlobalLoading}>
                                                        <Clerk.Loading>
                                                            {(isLoading) => {
                                                                return isLoading ? (
                                                                    <Loader2 className="size-4 animate-spin" />
                                                                ) : (
                                                                    'Continue'
                                                                )
                                                            }}
                                                        </Clerk.Loading>
                                                    </Button>
                                                </SignIn.Action>
                                                <SignIn.Action navigate="choose-strategy" asChild>
                                                    <Button size="sm" variant="link">
                                                        Use another method
                                                    </Button>
                                                </SignIn.Action>
                                            </div>
                                        </CardFooter>
                                    </Card>
                                </SignIn.Strategy>
                            </SignIn.Step>
                        </>
                    )}
                </Clerk.Loading>
            </SignIn.Root>
        </div>
    )
}